const selectId = document.querySelector('#location-options');

// Populate the select dropdown with city options
cityData.forEach(cityObject => {
    const option = document.createElement('option');
    option.classList.add('options');
    option.value = cityObject.city;
    option.textContent = cityObject.city;
    selectId.appendChild(option);
});

let generateOrderHtml = '';

// Event listener for the select input change
selectId.addEventListener('change', () => {
    const selectedCity = selectId.value;
    document.querySelector('.lms-title').innerText = `Popular localities in and around ${selectedCity}`;
    clearLocalitiesGrid();

    const selectedCityData = cityData.find(city => city.city === selectedCity);
    if (selectedCityData) {
        selectedCityData.areas.forEach((area, index) => {
            createCityDiv(area, index);
        });
    }
});

function createCityDiv(area, index) {
    const container = document.querySelector('.grid-localities');
    const cityLocalities = document.createElement('div');
    cityLocalities.classList.add('city-localities');

    const citynameAndPlaces = document.createElement('div');
    citynameAndPlaces.classList.add('cityname-and-places');

    const p1 = document.createElement('p');
    p1.classList.add('p1');
    p1.textContent = area;

    const p2 = document.createElement('p');
    p2.classList.add('p2');
    p2.textContent = '6 places';

    citynameAndPlaces.appendChild(p1);
    citynameAndPlaces.appendChild(p2);

    const anchorElement = document.createElement('a');
    anchorElement.href = 'order.html';
    anchorElement.target = '_blank';

    const arrowIconDiv = document.createElement('div');
    arrowIconDiv.classList.add('arrow-icon-div');
    arrowIconDiv.onclick = () => {
        generateOrderItems(getFoodItemsStartIndex(index));
    };

    const arrowIconImage = document.createElement('img');
    arrowIconImage.src = 'images/next.png';
    arrowIconImage.alt = '';
    arrowIconImage.classList.add('arrow-icon-image');

    arrowIconDiv.appendChild(arrowIconImage);
    anchorElement.appendChild(arrowIconDiv);

    cityLocalities.appendChild(citynameAndPlaces);
    cityLocalities.appendChild(anchorElement);

    container.appendChild(cityLocalities);
}

function clearLocalitiesGrid() {
    document.querySelector('.grid-localities').innerHTML = '';
}

export function getFoodItemsStartIndex(index) {
    return index * 6;
}

function generateOrderItems(startIndex) {
     // Clear previous HTML content
    const endIndex = Math.min(startIndex + 6, foodItems.length);
    for (let i = startIndex; i < endIndex; i++) {
        generateOrderHtml += `
            <div class="order-div">
                <div class="order-image-div">
                    <img src="${foodItems[i].image}" class="order-image">
                    <span class="offer-div">${foodItems[i].offer}</span>
                </div>
                <p class="itemName">${foodItems[i].name} <span class="rating-span">${foodItems[i].rating} &#x2605;</span></p>
                <p class="price-and-time">&#x20B9;${foodItems[i].price} for one <span class="time">${foodItems[i].time} min</span></p>
            </div>
        `;
    }
    document.querySelector('.order-grid').innerHTML = generateOrderHtml;
}

